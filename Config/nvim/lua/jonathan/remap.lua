-- Toggle the location list with diagnostics
function _G.toggle_loc_list()
  local loclist_open = vim.fn.getloclist(0, { winid = 0 }).winid ~= 0
  if loclist_open then
    vim.cmd.lclose()
  else
    vim.diagnostic.setloclist({ open = true })
  end
end

-- Map <leader>e to toggle the location list
vim.keymap.set('n', '<leader>e', '<cmd>lua toggle_loc_list()<CR>', { silent = true })

